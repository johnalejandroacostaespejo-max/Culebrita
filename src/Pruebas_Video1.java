public class Pruebas_Video1 {
    public static void main(String[] args) {
        // 1. Declara una variable de tipo String y asígnale tu nombre.
        String Name = "John";
        System.out.println(Name);
        // 2. Crea una variable de tipo int y asígnale tu edad.
        int age = 29;
        System.out.println(age);
        // 3. Crea una variable double con tu altura en metros.
        double height = 1.74;
        System.out.println(height);
        // 4. Declara una variable de tipo boolean que indique si te gusta programar.
        boolean likesProgramming = true;
        likesProgramming = false;
        System.out.println(likesProgramming);
        // 5. Declara una constante con tu email.
        final String Email = "John.java@aprendiendo.com";
        System.out.println(Email);
        // 6. Crea una variable de tipo char y guárdale tu inicial.
        char initial = 'J';
        System.out.println(initial);
        // 7. Declara una variable de tipo String con tu localidad, y a continuación cambia su valor y vuelve a imprimirla.
        String Locality = "Soacha";
        System.out.println(Locality);
        Locality = "Cali";
        // 8. Crea una variable int llamada a, otra b, e imprime la suma de ambas.
        int a = 3;
        int b = 7;
        System.out.println("La suma de a+b es: " + (a + b));
        // 9. Imprime el tipo de dos variables creadas anteriormente.
        System.out.println("Mi nombre es: " + Name + ", vivo en: " + Locality);
        // 10. Intenta declarar una variable sin inicializarla y luego asígnale un valor antes de imprimirla.
        int vari;
        vari = 10;
        System.out.println(vari);

    }
}
